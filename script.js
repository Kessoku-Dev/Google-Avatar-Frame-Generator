// CANVAS RENDERING LOGIC
// ----------------------------------------------------

function drawRing(ctx, size, borderWidth) {
  const center = size / 2;
  const radius = center - borderWidth / 2;

  ctx.save();
  ctx.lineWidth = borderWidth;
  ctx.lineCap = "butt";

  const segments = PALETTES[selectedPalette] || PALETTES.classic;

  segments.forEach((segment) => {
    ctx.beginPath();
    ctx.strokeStyle = segment.color;
    ctx.arc(
      center,
      center,
      radius,
      (segment.start * Math.PI) / 180,
      (segment.end * Math.PI) / 180,
      false
    );
    ctx.stroke();
  });

  ctx.restore();
}

function drawCircularAvatar(ctx, image, size, borderWidth, gapWidth) {
  const avatarDiameter = size - 2 * (borderWidth + gapWidth);
  const avatarX = borderWidth + gapWidth;
  const avatarY = borderWidth + gapWidth;

  // Read zoom & offset adjustments
  const zoomFactor = parseFloat(zoomInput.value) / 100;
  const offsetXPercent = parseFloat(offsetXInput.value) / 100;
  const offsetYPercent = parseFloat(offsetYInput.value) / 100;

  // Source clipping bounds
  const minDim = Math.min(image.width, image.height);
  const sourceCropSize = minDim / zoomFactor;

  // Panning logic: offset is scaled relative to the available scroll range
  const maxShiftX = (image.width - sourceCropSize) / 2;
  const maxShiftY = (image.height - sourceCropSize) / 2;
  
  // Calculate crop coordinates on the source image
  const cropX = (image.width - sourceCropSize) / 2 + (offsetXPercent * image.width);
  const cropY = (image.height - sourceCropSize) / 2 + (offsetYPercent * image.height);

  ctx.save();
  ctx.beginPath();
  ctx.arc(size / 2, size / 2, avatarDiameter / 2, 0, Math.PI * 2);
  ctx.clip();
  
  // Draw avatar
  ctx.drawImage(
    image,
    cropX,
    cropY,
    sourceCropSize,
    sourceCropSize,
    avatarX,
    avatarY,
    avatarDiameter,
    avatarDiameter
  );
  ctx.restore();
}

function renderAvatar(image) {
  if (!image) return;

  const outputSize = Math.min(image.width, image.height);
  
  // Ratios
  const borderRatio = parseFloat(ringWidthInput.value) / 100;
  const gapRatio = parseFloat(gapWidthInput.value) / 100;

  const borderWidth = Math.max(1, Math.round(outputSize * borderRatio));
  const gapWidth = Math.max(0, Math.round(outputSize * gapRatio));

  outputCanvas.width = outputSize;
  outputCanvas.height = outputSize;
  outputCanvas.style.display = "block";
  outputPlaceholder.style.display = "none";

  const ctx = outputCanvas.getContext("2d");
  ctx.clearRect(0, 0, outputSize, outputSize);

  // 1. Draw Google Color Ring
  drawRing(ctx, outputSize, borderWidth);

  // 2. Draw White Spacer Background Disk
  ctx.fillStyle = "#FFFFFF";
  ctx.beginPath();
  ctx.arc(
    outputSize / 2,
    outputSize / 2,
    outputSize / 2 - borderWidth,
    0,
    Math.PI * 2
  );
  ctx.fill();

  // 3. Draw cropped, zoomed, and offset avatar
  drawCircularAvatar(ctx, image, outputSize, borderWidth, gapWidth);

  // 4. Update data export url & enable download
  try {
    outputDataUrl = outputCanvas.toDataURL("image/png");
    downloadButton.disabled = false;

    // 5. Update Status Text (localized)
    if (currentLang === "en") {
      setStatus(`Generated ${outputSize}x${outputSize} PNG with a ${borderWidth}px color ring and ${gapWidth}px white spacer.`);
    } else {
      setStatus(`已生成 ${outputSize}x${outputSize} 像素的 PNG 图像（含 ${borderWidth}px 彩环与 ${gapWidth}px 留白）。`);
    }
  } catch (err) {
    console.warn("Canvas export blocked by CORS policy:", err);
    outputDataUrl = "";
    downloadButton.disabled = true;

    if (currentLang === "en") {
      setStatus("CORS restriction on the preset image prevents direct download. Please upload an image manually to download.");
    } else {
      setStatus("受限于预设图片的跨域策略（CORS），无法直接下载该头像。请手动上传本地图片以支持导出下载。");
    }
  }
}


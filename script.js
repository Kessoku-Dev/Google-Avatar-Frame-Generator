const PRESET_URL = "https://img.998315.xyz/api/Pic/avatar.jpg";

function initPresets() {
  const btn = document.createElement("button");
  btn.className = "preset-item active";
  btn.title = "Preset Avatar";

  const img = document.createElement("img");
  img.src = PRESET_URL;
  img.alt = "Preset Avatar";
  img.crossOrigin = "anonymous";

  btn.appendChild(img);
  presetsContainer.appendChild(btn);

  // Preload the image
  const image = new Image();
  image.crossOrigin = "anonymous";
  image.onload = () => {
    inputPreview.src = PRESET_URL;
    inputPreview.style.display = "block";
    inputPlaceholder.style.display = "none";

    activeImage = image;
    renderAvatar(image);
  };
  image.onerror = () => {
    // Fallback: If CORS fails, try standard load (without anonymous)
    console.warn("Could not load image with anonymous CORS, trying standard loading.");
    
    const fallbackImage = new Image();
    fallbackImage.onload = () => {
      inputPreview.src = PRESET_URL;
      inputPreview.style.display = "block";
      inputPlaceholder.style.display = "none";
      activeImage = fallbackImage;
      renderAvatar(fallbackImage);
    };
    fallbackImage.src = PRESET_URL;
  };
  image.src = PRESET_URL;

  // Click handler
  btn.addEventListener("click", () => {
    document.querySelectorAll(".preset-item").forEach(item => item.classList.remove("active"));
    btn.classList.add("active");
    
    inputPreview.src = PRESET_URL;
    inputPreview.style.display = "block";
    inputPlaceholder.style.display = "none";

    if (activeImage) {
      renderAvatar(activeImage);
    }
  });
}


// FILE UPLOAD AND DRAG-AND-DROP HANDLERS
// ----------------------------------------------------

function handleFile(file) {
  if (!file) return;

  if (!file.type.startsWith("image/")) {
    setStatus(TRANSLATIONS[currentLang]["status-loaded-err"]);
    return;
  }

  setStatus(TRANSLATIONS[currentLang]["status-rendering"]);
  const fileUrl = URL.createObjectURL(file);
  const image = new Image();

  image.onload = () => {
    // Clear active preset buttons styling
    document.querySelectorAll(".preset-item").forEach(item => item.classList.remove("active"));

    inputPreview.src = fileUrl;
    inputPreview.style.display = "block";
    inputPlaceholder.style.display = "none";
    
    activeImage = image;
    renderAvatar(image);
    URL.revokeObjectURL(fileUrl);
  };

  image.onerror = () => {
    URL.revokeObjectURL(fileUrl);
    setStatus(TRANSLATIONS[currentLang]["status-error"]);
  };

  image.src = fileUrl;
}

fileInput.addEventListener("change", (event) => {
  const [file] = event.target.files;
  handleFile(file);
});

// Drag and drop event listeners
["dragenter", "dragover"].forEach(eventName => {
  dropZone.addEventListener(eventName, (e) => {
    e.preventDefault();
    dropZone.classList.add("dragover");
  }, false);
});

["dragleave", "drop"].forEach(eventName => {
  dropZone.addEventListener(eventName, (e) => {
    e.preventDefault();
    dropZone.classList.remove("dragover");
  }, false);
});

dropZone.addEventListener("drop", (e) => {
  const dt = e.dataTransfer;
  const file = dt.files[0];
  handleFile(file);
}, false);


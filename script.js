function updateTranslations() {
  // Translate static data-i18n attributes
  document.querySelectorAll("[data-i18n]").forEach(element => {
    const key = element.getAttribute("data-i18n");
    if (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key]) {
      if (element.tagName === "INPUT" && element.placeholder) {
        element.placeholder = TRANSLATIONS[currentLang][key];
      } else {
        element.textContent = TRANSLATIONS[currentLang][key];
      }
    }
  });

  // Update Language Button label
  langToggle.querySelector(".lang-text").textContent = currentLang === "en" ? "ZH" : "EN";
  langToggle.title = currentLang === "en" ? "切换至中文" : "Switch to English";

  // Re-translate status bar if active
  if (!activeImage) {
    setStatus(TRANSLATIONS[currentLang]["status-waiting"]);
  } else {
    // Re-render to update the status text in the correct language
    renderAvatar(activeImage);
  }
}

function setStatus(message) {
  status.textContent = message;
}

// Toggle language
langToggle.addEventListener("click", () => {
  currentLang = currentLang === "en" ? "zh" : "en";
  localStorage.setItem("g-avatar-lang", currentLang);
  updateTranslations();
});

// Theme Toggle logic
function initThemeUI() {
  // Check if system prefers dark and no theme is stored
  if (!localStorage.getItem("g-avatar-theme") && window.matchMedia("(prefers-color-scheme: dark)").matches) {
    currentTheme = "dark";
    document.documentElement.setAttribute("data-theme", "dark");
  }
}

themeToggle.addEventListener("click", () => {
  currentTheme = currentTheme === "light" ? "dark" : "light";
  document.documentElement.setAttribute("data-theme", currentTheme);
  localStorage.setItem("g-avatar-theme", currentTheme);
});

